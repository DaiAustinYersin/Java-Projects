    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if (Validator()) {
            return;
        }
        try {
            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txtMaSV.getText());
            bd.setTiengAnh(Double.parseDouble(txtTgAnh.getText()));
            bd.setTinHoc(Double.parseDouble(txtTin.getText()));
            bd.setGDTC(Double.parseDouble(txtGDTC.getText()));

            QuanLyBangDiem qlbd = new QuanLyBangDiem();
            if (qlbd.findByMaSV(txtMaSV.getText()) != null) {
                if (jSupport.ShowDialog.showConfirm(parent, "Xác nhận", "Bạn có muốn cập nhật điểm sinh viên mã " + txtMaSV.getText() + " không?") != JOptionPane.YES_OPTION) {
                    return;
                }
                if (qlbd.update(bd)) {
                    loadDataToTable(3);
                    jSupport.ShowDialog.showMessage(parent, "Thông báo", "Dữ liệu sinh viên mã " + txtMaSV.getText() + " đã được cập nhật");
                } else {
                    jSupport.ShowDialog.showMessage(parent, "Thông báo", "Dữ liệu chưa được cập nhật do lỗi");
                }
                return;
            }

            if (qlbd.insert(bd)) {
                list.add(bd);
                NumberFormat format = new DecimalFormat("#0.00");
                String diemTB = format.format((bd.getTiengAnh() + bd.getTinHoc() + bd.getGDTC()) / 3);
                tableModel.addRow(new Object[]{
                    bd.getMaSinhVien(), bd.getHoTen(), bd.getTiengAnh(), bd.getTinHoc(), bd.getGDTC(),
                    diemTB
                });
                loadDataToTable(3);
                jSupport.ShowDialog.showMessage(parent, "Thông báo", "Dữ liệu sinh viên mã " + txtMaSV.getText() + " đã được lưu");
            } else {
                jSupport.ShowDialog.showMessage(parent, "Thông báo", "Bảng điểm chưa được lưu do lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
            jSupport.ShowDialog.showError(parent, "Lỗi", e.getMessage());
        }
    }//GEN-LAST:event_btnLuuActionPerformed

    private boolean Validator() {
        sb = new StringBuilder();
        Validator.checkNull(txtMaSV, "Chưa nhập mã sinh viên", sb);
        Validator.checkDiem(txtTgAnh, sb, "Điểm phải trong khoảng 1-10", "Điểm phải là một số", "Điểm tiếng Anh chưa nhập");
        Validator.checkDiem(txtTin, sb, "Điểm phải trong khoảng 1-10", "Điểm phải là một số", "Điểm Tin học chưa nhập");
        Validator.checkDiem(txtGDTC, sb, "Điểm phải trong khoảng 1-10", "Điểm phải là một số", "Điểm GDTC chưa nhập");
        if (sb.length() > 0) {
            jSupport.ShowDialog.showError(parent, "Dữ liệu không hợp lệ", sb.toString());
            return true;
        }
        return false;
    }

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        sb = new StringBuilder();
        Validator.checkNull(txtMaSV, "Mã sinh viên chưa nhập", sb);
        if (sb.length() > 0) {
            jSupport.ShowDialog.showError(parent, "Dữ liệu không hợp lệ", sb.toString());
            return;
        }
        try {
            QuanLyBangDiem qlbd = new QuanLyBangDiem();
            BangDiem bd = qlbd.findByMaSV(txtMaSV.getText());
            if (bd == null) {
                jSupport.ShowDialog.showMessage(parent, "Mã sinh viên không tồn tại", "Dữ liệu không hợp lệ");
                return;
            }
            if (jSupport.ShowDialog.showConfirm(parent, "Xác nhận", "Bạn có muốn xóa hay không?") != JOptionPane.YES_OPTION) {
                return;
            }
            if (qlbd.deleteByMaSV(bd)) {
                jSupport.ShowDialog.showError(parent, "Thông báo", "Điểm của sinh viên đã được xóa");
                loadDataToTable(3);
            } else {
                jSupport.ShowDialog.showError(parent, "Thông báo", "Điểm của sinh viên không được xóa do lỗi");
            }
        } catch (Exception e) {
            e.printStackTrace();
            jSupport.ShowDialog.showError(parent, "Lỗi", e.getMessage());
        }
    }//GEN-LAST:event_btnXoaActionPerformed

