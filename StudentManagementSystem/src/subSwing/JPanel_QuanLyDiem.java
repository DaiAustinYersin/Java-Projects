    private void txtTgAnhFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTgAnhFocusLost
        try {
            if (txtTgAnh.getText().isEmpty()
                    || txtTin.getText().isEmpty()
                    || txtGDTC.getText().isEmpty()) {
                return;
            }
            float ta = Float.parseFloat(txtTgAnh.getText());
            float it = Float.parseFloat(txtTin.getText());
            float pt = Float.parseFloat(txtGDTC.getText());

            float avg = (ta + it + pt) / 3;

            String st = String.format("%.2f", avg);
            lblDiemTB.setText(st);
        } catch (Exception e) {
            e.printStackTrace();
            jSupport.ShowDialog.showError(parent, "Lỗi", e.getMessage());
        }
    }//GEN-LAST:event_txtTgAnhFocusLost

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        try {
            tableModel.setRowCount(0);
            QuanLyBangDiem qlbd = new QuanLyBangDiem();
            if (qlbd.findByMaSV(txtFindMaSV.getText()) != null) {
                BangDiem bd = qlbd.findByMaSV(txtFindMaSV.getText());
                NumberFormat format = new DecimalFormat("#0.00");
                String diemTB = format.format((bd.getTiengAnh() + bd.getTinHoc() + bd.getGDTC()) / 3);
                tableModel.addRow(new Object[]{
                    bd.getMaSinhVien(), bd.getHoTen(), bd.getTiengAnh(), bd.getTinHoc(), bd.getGDTC(),
                    diemTB
                });
                index = 0;
                display();
            } else {
                clearForm();
                jSupport.ShowDialog.showMessage(parent, "Dữ liệu không hợp lệ", "Không tìm thấy thông tin sinh viên có mã " + txtFindMaSV.getText());
            }
        } catch (Exception e) {
            e.printStackTrace();
            jSupport.ShowDialog.showError(parent, "Lỗi", e.getMessage());
        }
    }//GEN-LAST:event_btnFindActionPerformed

